public class NotSupportedException extends BaseException {

    public NotSupportedException() {
        super();
    }

    public NotSupportedException(String message) {
        super(message);
    }

    public NotSupportedException(String format, String arg) {
        super(format, arg);
    }

    public NotSupportedException(String format, String arg1, String arg2) {
        super(format, arg1, arg2);
    }

    public NotSupportedException(String format, String arg1, String arg2, String arg3) {
        super(format, arg1, arg2);
    }

    public NotSupportedException(String format, String arg1, String arg2, String arg3, String arg4) {
        super(format, arg1, arg2, arg3, arg4);
    }

    public NotSupportedException(Throwable cause) {
        super(cause);
    }
}
